   <h3>File a Claim</h3>
   <p>At DSR Moving, we pride ourselves in treating your move with caution and care.  In the event that something gets damaged or lost, we will resolve the issue in the a fair and timely manner.  </p>

   <div class="divider"></div>
   <div class="section">
      <h5>Reporting a damaged or missing item.</h5>
      <p>If you feel that DSR has lost or damaged an item, please email us within 10 business days of the move at <a href="mailto:dsrmoving@gmail.com">dsrmoving@gmail.com</a> and we will send you a claim form.</p>
   </div>
      
