   <div class="section">
      <h5>Protecting your move.</h5>
      <p> You must select your level of valuation before the move begins. Once it starts, the selection cannot be changed. Also, be sure to provide the mover with a list of items you believe to be of extraordinary value (see Articles of Extraordinary Value). While preparing that list, customers sometimes realize that they have undervalued their shipment by simply accepting the minimum required. If everything on the truck is destroyed, are you prepared to accept a check for the value of the shipment shown on the estimate? If not, talk to someone about declaring an increased amount (and paying a higher fee).   </p>

      <p>   Basic Value Protection - No Charge: Mover’s maximum liability will be $0.60 per pound based upon the weight of any lost or damaged items, regardless of its actual value. For example, damage to your refrigerator weighing 400 pounds would result in a maximum claim settlement of $240. Basic Value Protection provides minimal protection, and it is possible that settlement of any claim under this level of valuation will not be satisfactory to you. Under this type of valuation, for example, if the total weight of your shipment is 8,000 pounds, then the total value of your entire shipment is established to be $4,800.   </p>

      <p>   Full Value Protection - $0.75 Per $100 of Value: The minimum value of the shipment will be $4.00 times the weight of the shipment. However, you have the right to declare that your shipment has a greater value and pay for that increased protection. If items are lost, the mover will have the options of replacing them with articles of like kind and quality or paying the replacement cost as determined by current market value. If items are damaged, the mover will have the same options, plus the additional options of repairing the items or paying the repair cost. All damaged items that are either replaced or reimbursed at full-market value become the property of the mover. Under this type of valuation, for example, if the total weight of your shipment is 8,000 pounds, then the total value of your entire shipment is established to be $32,000 and the charge for that level of protection would be $240. However, if you decided that your shipment has a greater value, maybe $45,000 rather than the calculated minimum of $32,000, you could establish that your shipment value is $45,000 and the charge for that level of protection would be $337.50. </p>
   </div>


   <div class="divider"></div>
   <div class="section">
      <h5>How are settlements determined</h5>
      <p> If you have lost or damaged items, contact your mover for a claim form. Claims will not be processed until you pay for the move in full. CLAIMS SHOULD BE FILED WITH THE MOVER AS SOON AS POSSIBLE, BUT NO LATER THAN NINE MONTHS AFTER DELIVERY. DO NOT DISPOSE OF ANY DAMAGED ITEMS OR THE PACKING MATERIALS USED FOR THOSE ITEMS SINCE THEY WILL BE NEEDED FOR PROOF OF DAMAGE.
      </p>   
         <p>
   On the claim form, list all damaged and lost items, including the age, original cost, and weight as well as a description of the damage. The mover may request estimates or may send a third party to assess the damages.
      </p>   <p>
   All claims will be settled based upon the type of valuation you selected on the Bill of Lading and the Addendum to the Bill of Lading (see Valuation section above). If you are unable to resolve your claim with the mover, the Public Staff’s Transportation Rates Division (919-733-7766) is available to assist you. If the mover denies liability for all or part of your claim, you may seek legal action against the mover. However, such action must be taken within two years and one day from the date when written notice was given by the mover to you that your claim has been denied. Items believed to be stolen should be reported to the proper law enforcement authorities immediately.  </p>
   </div>


   
      
